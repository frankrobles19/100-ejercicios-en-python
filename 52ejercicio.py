# Hacer un algoritmo que muestre el área y perímetro de un pentágono

print("AREA DEL RECTANGULO")
base = int(input("ingrese base"))
altura = int(input("ingrese altura"))

a = base * altura
print("AREA: ", a, "cm")

print("PERIMETRO DEL RECTANGULO")

p = (2*altura) + (2*base)
print("PERIMETRO", p, "cm")

